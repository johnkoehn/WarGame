package Wargame;

public class Point {
private float xpos;
private float ypos;
//construct a point using x and y values
public Point(float xpos, float ypos)
{this.xpos = xpos;
this.ypos = ypos;
}
//return x value
public float getX()
{return xpos;}
//return y value
public float getY()
{return ypos;}


}
